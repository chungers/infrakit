    "-e REGION=$AWS_REGION ",
    "-e STACK_NAME=$STACK_NAME ",
    "-e INSTANCE_NAME='ManagerAsg' ",
    "-e VPC_ID=$VPC_ID ",
    "-e STACK_ID=\"$STACK_ID\" ",
    "-e ACCOUNT_ID=$ACCOUNT_ID ",
    "-e SWARM_QUEUE=\"$SWARM_QUEUE\" ",
    "-e CLEANUP_QUEUE=\"$CLEANUP_QUEUE\" ",
