    "-v /var/log:/var/log ",
    "docker4x/init-aws:$DOCKER_FOR_IAAS_VERSION\n",

    "docker run --label com.docker.editions.system --log-driver=json-file ",
    "--name=guide-aws --restart=always -d ",
